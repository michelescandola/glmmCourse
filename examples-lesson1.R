# Il prezzo e la spesa in attività promozionali sono due dei fattori che determinano in maniera
# preponderante le vendite di un prodotto. Supponete che una grande catena di negozi alimentari
# operante su scala nazionale intenda introdurre una barretta energetica di basso prezzo, chiamata
# Omnipower. Le barrette energetiche contengono grassi, carboidrati e calorie e forniscono rapidamente
# energie ai corridori, agli scalatori e agli atleti in genere impegnati in lunghe ed estenuanti attività
# sportive. Le vendite delle barrette energetiche sono esplose negli ultimi anni e il grande magazzino
# ritiene che vi possa essere un buon mercato per la Omnipower. Prima di introdurre la barretta in tutti
# i magazzini, la divisione di marketing della catena intende stabilire l'effetto che il prezzo e le
# promozioni all'interno dei negozi possono avere sulle vendite.
# 
